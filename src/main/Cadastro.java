    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if ((Arrays.toString(pssSenhaC.getPassword())).equals((Arrays.toString(pssSenhaC2.getPassword())))) {
          
            monitor.setLogin(txtLoginC.getText());
            monitor.setNome(txtNomeC.getText());
            monitor.setSenha(Arrays.toString(pssSenhaC.getPassword()));
            monitor.setMatricula(txtMatC.getText());
            

            try {
                FileOutputStream fileout = new FileOutputStream(f);
                ObjectOutputStream escritor = new ObjectOutputStream(fileout);
                escritor.writeObject(monitor);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Cadastro.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Cadastro.class.getName()).log(Level.SEVERE, null, ex);
            }
           
            
            JOptionPane.showMessageDialog(this, "Cadastro Realizado Com Sucesso!");
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Senhas N√£o Correspondem!");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

