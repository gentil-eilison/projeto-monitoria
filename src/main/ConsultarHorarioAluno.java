    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel table = new DefaultTableModel();
        jTable1.setModel(table);
        table.addColumn("Dia");
        table.addColumn("Hora");
        for (int i = 0; i < moni.getTurmas().get(moni.getTurmas().indexOf(((Turma)jComboBox1.getSelectedItem()))).getHorarios().size(); i++) {
            table.addRow(new Object[]{a.consultarHorario((Turma) jComboBox1.getSelectedItem()).get(i).getDia(), a.consultarHorario((Turma) jComboBox1.getSelectedItem()).get(i).getHora()});
        }
    }//GEN-LAST:event_jButton1ActionPerformed

